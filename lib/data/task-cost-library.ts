import type { RefurbTaskTemplate, TaskCostLibraryEntry } from "@/types/refurb"
import { TRADE_RATES } from "@/lib/data/trade-rates"

function mapToRefurbTaskTemplate(task: TaskCostLibraryEntry): RefurbTaskTemplate {
  const notes = [
    task.riskNotes.trim(),
    task.dependencyNotes.trim() !== "None" ? `Dependency: ${task.dependencyNotes.trim()}` : "",
  ]
    .filter(Boolean)
    .join(" ")

  return {
    id: task.id,
    room: task.roomType,
    scope: task.scopeLevel,
    taskName: task.taskName,
    trade: task.tradeRequired,
    labourDays: task.labourDays,
    dayRate: TRADE_RATES[task.tradeRequired].defaultDayRate,
    materialCost: task.materialCost,
    scalingRule: task.scalingRule,
    dependency: task.dependencyTaskId,
    notes,
  }
}

